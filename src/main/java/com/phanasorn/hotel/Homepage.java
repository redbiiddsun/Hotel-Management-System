    private void checkAvaBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkAvaBtnActionPerformed
        // TODO add your handling code here:
        LocalDate checkin = DateUtil.toLocalDate(checkInTxt.getText());
        LocalDate checkout = DateUtil.toLocalDate(checkOutTxt.getText());
        
        String firstName = firstNameTxt.getText();
        String lastName = lastNameTxt.getText();
        String phone = phoneTxt.getText();
        String IDPass = IDPassTxt.getText();
        
        int numofRoom = (int) roomSpinner.getValue();
        int guestNum = (int) guestSpinner.getValue();
        
        if (firstName.isEmpty() == false 
                && lastName.isEmpty() == false
                && phone.isEmpty() == false
                && IDPass.isEmpty() == false
                && numofRoom != 0
                && guestNum != 0){
            
            if(conditionDate(checkin, checkout) == true){
                ConfirmDialog cf = new ConfirmDialog(1,new Reservations(generateBookingReference(),checkin,checkout,firstName,
                    lastName,IDPass,phone,guestNum,numofRoom,false,false));
                cf.setVisible(true);
            }else{
                return;
            }
            
        }else{
            JOptionPane.showMessageDialog(null, "Please fill the all information", "Alert",JOptionPane.WARNING_MESSAGE);
        }     
    }//GEN-LAST:event_checkAvaBtnActionPerformed


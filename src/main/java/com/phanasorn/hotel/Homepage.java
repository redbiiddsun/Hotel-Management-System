    public static String generateBookingReference() {
        String characters = "ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789";
        String reference = "";
        Random random = new Random();
        for (int i = 0; i < 5; i++) {
            reference += characters.charAt(random.nextInt(characters.length()));
        }
        return reference;
    }
    
    private void confirmBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmBtnActionPerformed
        // TODO add your handling code here:
        String selectionCheck = String.valueOf(checkCB.getSelectedItem());
        String selectionGuestINFO = String.valueOf(guestInfoCB.getSelectedItem());
        String guestInfo = guestTxt.getText();
        
        if(selectionCheck.equals("Check-in")){
            switch(selectionGuestINFO){
                case("Booking number"):
                    queryWithBookingRef(2,guestInfo);
                    break;
                case("ID/Passport"):
                    queryWithIDPass(2,guestInfo);
                    break;
                case("Firstname"):
                    queryWithFirstName(2,guestInfo);
                    break;
                case("Lastname"):
                    queryWithLastName(2,guestInfo);
                    break;
                    
                case("Phone number"):
                    queryWithPhone(2, guestInfo);
                    break;
            }
        }else{
            switch(selectionGuestINFO){
                case("Booking number"):
                    queryWithBookingRef(3,guestInfo);
                    break;
                case("ID/Passport"):
                    queryWithIDPass(3,guestInfo);
                    break;
                case("Firstname"):
                    queryWithFirstName(3,guestInfo);
                    break;
                case("Lastname"):
                    queryWithLastName(3,guestInfo);
                    break;
                    
                case("Phone number"):
                    queryWithPhone(3, guestInfo);
                    break;
            }
        }    
    }//GEN-LAST:event_confirmBtnActionPerformed

